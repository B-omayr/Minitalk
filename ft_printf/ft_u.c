#include "libftprintf.h"

int	ft_u(unsigned int h)
{
	int	s;

	s = 0;
	if (h < 10)
		s = s + ft_putchar(h + 48);
	else
	{
		s += ft_u (h / 10);
		s += ft_u (h % 10);
	}
	return (s);
}
