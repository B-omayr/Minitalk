#include "libftprintf.h"

int	ft_putnbr(int h)
{
	int	s;

	s = 0;
	if (h == -2147483648)
	{
		write (1, "-2147483648", 11);
		return (11);
	}
	if (h < 0)
	{
		s = ft_putchar('-');
		h *= -1;
	}
	if (h < 10)
		s = s + ft_putchar(h + 48);
	else
	{
		s += ft_putnbr (h / 10);
		s += ft_putnbr (h % 10);
	}
	return (s);
}
